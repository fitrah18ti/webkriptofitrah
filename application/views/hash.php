                        Fungsi hash adalah fungsi matematis yang mengubah nilai input numerik menjadi nilai numerik yang terkompresi. Maksudnya bertujuan mengkompresi nilai numerik yang diinputkan. Inputan fungsi hash mempunyai panjang yang beragam, namun outputan nilai hash akan selalu mempunyai panjang yang tetap. Nilai yang dikembalikan oleh fungsi hash disebut message digest atau hanya nilai hash.
                        Fungsi hash kriptografi memang dirancang untuk mencegah mengembalikan checksum yang sudah di hash kembali ke teks asli. Namun, meskipun hampir tidak mungkin dibalik, itu bukan berarti nilai hash 100% dijamin aman.
                        Seperti sekarang banyak software yang memungkinkan untuk menterjemahkan kembali fungsi hash, seperti Rainbow Table yang dapat digunakan untuk mengcrack plaintext checksum. Rainbow Table pada dasarnya adalah kamus yang mengeluarkan ribuan, jutaan, atau bahkan milyaran dari ini di samping nilai plainteks yang sesuai.
                        Meskipun ini secara teknis tidak membalikkan algoritma hash kriptografi, hal itu mungkin juga karena sangat mudah dilakukan. Tapi kenyataanya Rainbow Table kadang tidak bisa membuat daftar setiap checksum yang mungkin ada, biasanya hanya “membantu” untuk menemukan frasa sederhana seperti password yang lemah. Tetapi untuk password yang lebih rumit atau mengandung symbol mungkin tidak bisa. Itulah mengapa hanya orang yang menghash password sendirilah yang tau.
